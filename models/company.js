    // if name is pressent, we add to filters array, turn it lowercase and check if there is a company name containing thatr in lowercase. then add to the values array.
    if(name) {
      filter.push("LOWER(name) LIKE $1");
      values.push(`%{name.toLowerCase()}%`);
    }

